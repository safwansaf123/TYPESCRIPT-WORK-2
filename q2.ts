let name: string = "saFwan ahmed";
console.log(name);// SIMPLE PRINT//
console.log(name.toLowerCase());//ALL IN LOWER CASE//
console.log(name.toUpperCase());//ALL IN upper CASE//
console.log(name.charAt(0).toUpperCase());//this will print first letter only//
console.log(name.charAt(3).toUpperCase());//this will print third letter only//
console.log(name.slice(1).toUpperCase());//this will remove the first letters//
console.log(name.slice(4).toUpperCase());//this will remove the first three letters// 
console.log(name.charAt(0).toUpperCase() + name.slice(1).toLowerCase());
